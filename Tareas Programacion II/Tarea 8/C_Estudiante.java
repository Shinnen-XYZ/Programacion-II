package service;

public class C_Estudiante {
    private int carnet;

    public C_Estudiante(){
        this.carnet = carnet;
    }

    public int getCarnet() {
        return carnet;
    }

    public void setCarnet(int carnet) {
        this.carnet = carnet;
    }
    public String toString(){
        return "C_Estudiante" + carnet;
    }
}
